package model;

/**
 * The SuggestionStatus enum is used to represent the status of a suggestion.
 * It is used in the Suggestion class.
 */
public enum SuggestionStatus {

	/**
	 * Represents a suggestion that is open.
	 */
	OPEN,
	/**
	 * Represents a suggestion that is closed.
	 */
	CLOSED,

	/**
	 * Represents a suggestion that is approved.
	 */

	APPROVED;
}
